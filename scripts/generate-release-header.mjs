import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const packageJson = JSON.parse(
  await fs.readFile(path.join(repoRoot, "package.json"), "utf8")
);

const args = parseArgs(process.argv.slice(2));
const version = args.version || packageJson.version;
const source = args.source || "public/favicon.svg";
const output = args.output || `docs/public/releases/release-header-v${version}.svg`;
const eyebrow = args.eyebrow || "RELEASE NOTES";
const title = args.title || `GeminiVRM v${version}`;
const subtitle =
  args.subtitle || "Generated from public/favicon.svg for the published release notes";
const callout = args.callout || "Browser-first VRM chat powered by Gemini Live";
const highlights = (args.highlights || [
  "Gemini Live native audio",
  "Bundled VRM avatar + idle motions",
  "Optional YouTube relay",
]).slice(0, 3);

const sourcePath = path.resolve(repoRoot, source);
const outputPath = path.resolve(repoRoot, output);

const faviconSvg = await fs.readFile(sourcePath, "utf8");
ensureSvgSource(faviconSvg, source);

await fs.mkdir(path.dirname(outputPath), { recursive: true });
const faviconHref = buildPublishedRelativeHref(outputPath, sourcePath);
const svg = buildReleaseHeaderSvg({
  eyebrow,
  title,
  subtitle,
  callout,
  highlights,
  faviconHref,
  source,
});

await fs.writeFile(outputPath, svg, "utf8");
console.log(`Generated ${path.relative(repoRoot, outputPath)} from ${source}`);

function parseArgs(argv) {
  const parsed = {};

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith("--")) {
      continue;
    }

    const key = token.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
      continue;
    }

    if (key === "highlights") {
      parsed[key] = next
        .split("|")
        .map((item) => item.trim())
        .filter(Boolean);
    } else {
      parsed[key] = next;
    }

    index += 1;
  }

  return parsed;
}

function ensureSvgSource(svg, sourcePathLabel) {
  if (!svg.includes("<svg")) {
    throw new Error(`Source asset is not an SVG: ${sourcePathLabel}`);
  }
}

function buildPublishedRelativeHref(outputFilePath, sourceFilePath) {
  const outputRelativePath = toPosix(path.relative(repoRoot, outputFilePath));
  const sourceRelativePath = toPosix(path.relative(repoRoot, sourceFilePath));

  const publishedOutputPath = toPublishedPath(outputRelativePath);
  const publishedSourcePath = toPublishedPath(sourceRelativePath);

  return toPosix(
    path.posix.relative(
      path.posix.dirname(publishedOutputPath),
      publishedSourcePath
    )
  );
}

function toPublishedPath(repoRelativePath) {
  if (repoRelativePath.startsWith("docs/public/")) {
    return `docs/${repoRelativePath.slice("docs/public/".length)}`;
  }

  if (repoRelativePath.startsWith("public/")) {
    return repoRelativePath.slice("public/".length);
  }

  return repoRelativePath;
}

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function escapeXml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function escapeAttribute(value) {
  return escapeXml(value).replaceAll('"', "&quot;");
}

